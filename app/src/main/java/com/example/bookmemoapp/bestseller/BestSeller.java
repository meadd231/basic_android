package com.example.bookmemoapp.bestseller;

public class BestSeller {
    int rank;
    String rank_image;
    String large_image;
    String rank_title;
    String rank_author;
    int rank_price;
    String description;
    String link;
    String publisher;

    public String getLarge_image() {
        return large_image;
    }

    public void setLarge_image(String large_image) {
        this.large_image = large_image;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getRank_image() {
        return rank_image;
    }

    public void setRank_image(String rank_image) {
        this.rank_image = rank_image;
    }

    public String getRank_title() {
        return rank_title;
    }

    public void setRank_title(String rank_title) {
        this.rank_title = rank_title;
    }

    public String getRank_author() {
        return rank_author;
    }

    public void setRank_author(String rank_author) {
        this.rank_author = rank_author;
    }

    public int getRank_price() {
        return rank_price;
    }

    public void setRank_price(int rank_price) {
        this.rank_price = rank_price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }
}
